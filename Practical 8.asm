Clockwise Rotation
MOV P1, #00H

CW:
    MOV P1, #08H
    ACALL DELAY
    MOV P1, #04H
    ACALL DELAY
    MOV P1, #02H
    ACALL DELAY
    MOV P1, #01H
    ACALL DELAY
    SJMP CW

DELAY:
    MOV R6, #255
D1: MOV R7, #255
D2: DJNZ R7, D2
    DJNZ R6, D1
    RET

Anticlockwise Rotation
MOV P1, #00H

ACW:
    MOV P1, #01H
    ACALL DELAY
    MOV P1, #02H
    ACALL DELAY
    MOV P1, #04H
    ACALL DELAY
    MOV P1, #08H
    ACALL DELAY
    SJMP ACW
